USE DATABASE SALES_ANALYTICS_DB;
USE SCHEMA MART;

-- Total Sales

SELECT
SUM(SALES) AS TOTAL_SALES
FROM FACT_SALES;

-- Sales by Category

SELECT

P.CATEGORY,

SUM(F.SALES) AS TOTAL_SALES

FROM FACT_SALES F

JOIN DIM_PRODUCT P
ON F.PRODUCT_ID = P.PRODUCT_ID

GROUP BY P.CATEGORY

ORDER BY TOTAL_SALES DESC;

-- Top 10 Customers

SELECT

C.CUSTOMER_NAME,

SUM(F.SALES) AS TOTAL_SALES

FROM FACT_SALES F

JOIN DIM_CUSTOMER C
ON F.CUSTOMER_ID = C.CUSTOMER_ID

GROUP BY C.CUSTOMER_NAME

ORDER BY TOTAL_SALES DESC

LIMIT 10;