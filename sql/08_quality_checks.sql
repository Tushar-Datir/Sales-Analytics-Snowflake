USE DATABASE SALES_ANALYTICS_DB;
USE SCHEMA MART;

-- Total Rows

SELECT
COUNT(*) AS FACT_ROWS
FROM FACT_SALES;

-- Null Sales

SELECT
COUNT(*) AS NULL_SALES
FROM FACT_SALES
WHERE SALES IS NULL;

-- Duplicate Orders

SELECT
ORDER_ID,
COUNT(*) AS TOTAL

FROM FACT_SALES

GROUP BY ORDER_ID

HAVING COUNT(*) > 1;