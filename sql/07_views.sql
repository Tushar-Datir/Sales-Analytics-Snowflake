USE DATABASE SALES_ANALYTICS_DB;
USE SCHEMA MART;

CREATE OR REPLACE VIEW VW_SALES_SUMMARY AS

SELECT

    D.ORDER_YEAR,

    D.MONTH_NAME,

    P.CATEGORY,

    SUM(F.SALES) AS TOTAL_SALES,

    COUNT(DISTINCT F.ORDER_ID) AS TOTAL_ORDERS

FROM FACT_SALES F

JOIN DIM_PRODUCT P
ON F.PRODUCT_ID = P.PRODUCT_ID

JOIN DIM_DATE D
ON F.ORDER_DATE = D.ORDER_DATE

GROUP BY

    D.ORDER_YEAR,
    D.MONTH_NAME,
    P.CATEGORY;